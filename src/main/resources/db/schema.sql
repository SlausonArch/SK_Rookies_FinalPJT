-- ── TRANSACTIONS ─────────────────────────────────────────────────────────────
-- TX_TYPE: BUY/SELL(주문), DEPOSIT/WITHDRAW(입출금), FEE(수수료),
--          ADMIN_RECLAIM(관리자회수), ATTENDANCE_REWARD(출석보상), AD_MISSION_REWARD(광고미션보상)
BEGIN
    EXECUTE IMMEDIATE '
        CREATE TABLE TRANSACTIONS (
            TX_ID          NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
            MEMBER_ID      NUMBER NOT NULL,
            ORDER_ID       NUMBER,
            TX_TYPE        VARCHAR2(20) NOT NULL
                           CHECK (TX_TYPE IN (
                               ''BUY'', ''SELL'', ''DEPOSIT'', ''WITHDRAW'', ''FEE'',
                               ''ADMIN_RECLAIM'', ''ATTENDANCE_REWARD'', ''AD_MISSION_REWARD''
                           )),
            ASSET_TYPE     VARCHAR2(20) NOT NULL,
            AMOUNT         NUMBER(19,8) NOT NULL,
            PRICE          NUMBER(19,8),
            TOTAL_VALUE    NUMBER(19,8),
            FEE            NUMBER(19,8) DEFAULT 0,
            TX_DATE        TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
            FROM_ADDRESS   VARCHAR2(255),
            TO_ADDRESS     VARCHAR2(255),
            TX_HASH        VARCHAR2(255),
            BANK_NAME      VARCHAR2(50),
            ACCOUNT_NUMBER VARCHAR2(100),
            STATUS         VARCHAR2(20) DEFAULT ''COMPLETED'',
            CONSTRAINT FK_TX_MEMBER FOREIGN KEY (MEMBER_ID) REFERENCES MEMBERS(MEMBER_ID) ON DELETE CASCADE,
            CONSTRAINT FK_TX_ORDER  FOREIGN KEY (ORDER_ID)  REFERENCES ORDERS(ORDER_ID)

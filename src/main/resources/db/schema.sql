CREATE TABLE AUDIT_LOGS (
    LOG_ID          NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    MEMBER_ID       NUMBER,
    ACTION          VARCHAR2(100) NOT NULL,
    IP_ADDRESS      VARCHAR2(50),
    USER_AGENT      VARCHAR2(255),
    TARGET_TABLE    VARCHAR2(50),
    TARGET_ID       NUMBER,
    LOG_DETAIL      CLOB,
    CREATED_AT      TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX IDX_MEMBERS_EMAIL     ON MEMBERS(EMAIL);
CREATE INDEX IDX_ADMIN_EMAIL       ON ADMIN_MEMBERS(EMAIL);
CREATE INDEX IDX_POSTS_NOTICE      ON POSTS(IS_NOTICE DESC, CREATED_AT DESC);
CREATE INDEX IDX_TRANSACTIONS_MEMBER ON TRANSACTIONS(MEMBER_ID, TX_DATE DESC);
CREATE INDEX IDX_ORDERS_MEMBER     ON ORDERS(MEMBER_ID, STATUS);
CREATE INDEX IDX_AUDIT_ACTION      ON AUDIT_LOGS(ACTION, CREATED_AT DESC);

CREATE OR REPLACE TRIGGER TRG_UPDATE_MEMBER_TIER
AFTER INSERT ON TRANSACTIONS
FOR EACH ROW
DECLARE
    V_TOTAL_VOL NUMBER;
    V_NEW_TIER  NUMBER;
BEGIN
    SELECT NVL(SUM(TOTAL_VALUE), 0) INTO V_TOTAL_VOL
    FROM TRANSACTIONS
    WHERE MEMBER_ID = :NEW.MEMBER_ID
      AND TX_TYPE IN ('BUY', 'SELL');

    SELECT TIER_LEVEL INTO V_NEW_TIER
    FROM FEE_TIERS
    WHERE V_TOTAL_VOL >= MIN_VOLUME
      AND (V_TOTAL_VOL <= MAX_VOLUME OR MAX_VOLUME IS NULL)
    FETCH FIRST 1 ROW ONLY;

    UPDATE MEMBERS
    SET TIER_LEVEL = V_NEW_TIER
    WHERE MEMBER_ID = :NEW.MEMBER_ID;
END;
/


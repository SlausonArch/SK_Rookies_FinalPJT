-- ── 트리거 ──────────────────────────────────────────────────────────────────
CREATE OR REPLACE TRIGGER TRG_UPDATE_MEMBER_TIER
AFTER INSERT ON TRANSACTIONS
FOR EACH ROW
DECLARE
    V_TOTAL_VOL NUMBER;
    V_NEW_TIER  NUMBER;
BEGIN
    SELECT NVL(SUM(TOTAL_VALUE), 0) INTO V_TOTAL_VOL
    FROM TRANSACTIONS
    WHERE MEMBER_ID = :NEW.MEMBER_ID
      AND TX_TYPE IN ('BUY', 'SELL');

    SELECT TIER_LEVEL INTO V_NEW_TIER
    FROM FEE_TIERS
    WHERE V_TOTAL_VOL >= MIN_VOLUME
      AND (V_TOTAL_VOL <= MAX_VOLUME OR MAX_VOLUME IS NULL)
    FETCH FIRST 1 ROW ONLY;

    UPDATE MEMBERS
    SET TIER_LEVEL = V_NEW_TIER
    WHERE MEMBER_ID = :NEW.MEMBER_ID;
END;
/


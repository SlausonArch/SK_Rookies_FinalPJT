    CREATED_AT      TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_LIKES_MEMBER FOREIGN KEY (MEMBER_ID) REFERENCES MEMBERS(MEMBER_ID) ON DELETE CASCADE,
    CONSTRAINT UQ_LIKES_TARGET UNIQUE (MEMBER_ID, TARGET_TYPE, TARGET_ID)
